package wprowadzaniedanych;

public class MathematicalActions {

    double addResult;
    double minusResult;
    double multiplyResult;
    double divisionResult;


    public double add(double firstFigure, double secondFigure) {
        return addResult = firstFigure + secondFigure;

    }

    public double minus(double firstFigure, double secondFigure) {
        return minusResult = firstFigure - secondFigure;

    }

    public double multiply(double firstFigure, double secondFigure) {
        return multiplyResult = firstFigure * secondFigure;
    }

    public double division(double firstFigure, double secondFigure) {
        return divisionResult = firstFigure / secondFigure;
    }

}
