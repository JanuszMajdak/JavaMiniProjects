package petle;

public class WhileDo {
    public static void main(String[] args) {


        int i = 1;
        while (i < 5) {
            System.out.println("Liczba:" + i);
            i++;
        }
        System.out.println("---------------------------------");

        int k = 5;

        do {
            System.out.println("Liczba: " + i);
        } while (k < 3);
    }


}
